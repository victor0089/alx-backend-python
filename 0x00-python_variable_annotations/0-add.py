#!/usr/bin/env python3
"""
Module to perform addition of two float numbers.
"""


def add(a: float, b: float) -> float:
    """
    Adds two floats and returns their sum.

    Args:
        a (float): The first number.
        b (float): The second number.

    Returns:
        float: The sum of a and b.
    """
    return a + b
